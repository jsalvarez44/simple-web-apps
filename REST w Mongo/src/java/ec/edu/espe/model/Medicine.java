package ec.edu.espe.model;

/**
 *
 * @author SEBASTIAN
 */
public class Medicine {
    
    private String id;
    private String tipo;
    private String precio;

    public Medicine() {
    }

    public Medicine(String id, String tipo, String precio) {
        this.id = id;
        this.tipo = tipo;
        this.precio = precio;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the precio
     */
    public String getPrecio() {
        return precio;
    }

    /**
     * @param precio the precio to set
     */
    public void setPrecio(String precio) {
        this.precio = precio;
    }
    
    
    
}
